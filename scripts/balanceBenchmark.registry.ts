export function getSuiteById(id: string): SuiteDefinition | undefined {
  return BALANCE_BENCHMARK_REGISTRY.find((suite) => suite.id === id);
}

export function suitesForMode(mode: RunnerMode): SuiteDefinition[] {
  return BALANCE_BENCHMARK_REGISTRY.filter((suite) => suite.modes.includes(mode));
}

export function registryFamilies(): BenchmarkFamily[] {
  return Array.from(new Set(BALANCE_BENCHMARK_REGISTRY.map((suite) => suite.family)));
}
