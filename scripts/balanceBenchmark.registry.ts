type SuiteInput = Omit<RunnableSuiteDefinition, "command" | "arguments"> & {
  script: string;
  arguments?: string[];
};

function tsxSuite(input: SuiteInput): SuiteDefinition {
  return {
    ...input,
    command: nodeCommand,
    arguments: [npxCliPath, "--yes", "tsx", input.script, ...(input.arguments ?? [])],
  };
}

